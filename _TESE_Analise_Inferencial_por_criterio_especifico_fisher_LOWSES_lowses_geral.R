for (i in 1:12) {
  # Obter os vetores para cada par de c*
  todos_geral <- get(paste("c", i, "_todos_low_ses", sep = ""))
  todos_low_ses <- get(paste("c", i, "_todos_geral", sep = ""))
  
  # Criar os vetores de grupo
  group <- c(rep("todos_geral", length(todos_geral)), rep("todos_low_ses", length(todos_low_ses)))
  
  # Criar a tabela de contingência
  table_c <- table(Group = group, Response = factor(c(todos_geral, todos_low_ses), levels = 1:3))
  
  # Realizar o teste exato de Fisher
  test_c <- fisher.test(table_c)
  
  # Extrair o p-valor
  pvalue <- test_c$p.value
  
  # Decidir sobre o status de H0 e se houve diferença significativa
  if (is.nan(pvalue)) {
    h0_status <- "Não foi possível calcular"
    significant_difference <- "Não foi possível calcular"
  } else if (pvalue < 0.05) {
    h0_status <- "Rejeitamos H0"
    significant_difference <- "Houve diferença significativa"
  } else {
    h0_status <- "Não rejeitamos H0"
    significant_difference <- "Não houve diferença significativa"
  }
  
  # Decidir o indicador de valor-p
  p_value_indicator <- ifelse(pvalue < 0.05, "< 0.05", "> 0.05")
  
  # Adicionar os resultados ao dataframe
  results <- rbind(results, data.frame(C = paste("c", i, sep = ""),
                                       p_value = pvalue,
                                       p_value_indicator = p_value_indicator,
                                       h0_status = h0_status,
                                       significant_difference = significant_difference,
                                       stringsAsFactors = FALSE))
}

